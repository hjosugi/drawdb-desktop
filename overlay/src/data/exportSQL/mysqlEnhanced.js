// Enhanced MySQL SQL exporter for drawDB
const SAFE = (s) => "`" + String(s).replace(/`/g, "``") + "`";
const SAFE_LIT = (s) => "'" + String(s).replace(/\\/g, "\\\\").replace(/'/g, "''") + "'";
const TYPE_PASSTHRU = new Set([
  "INT","INTEGER","BIGINT","SMALLINT","TINYINT","MEDIUMINT",
  "DECIMAL","NUMERIC","FLOAT","DOUBLE","REAL",
  "VARCHAR","CHAR","TEXT","LONGTEXT","MEDIUMTEXT","TINYTEXT",
  "BLOB","LONGBLOB","MEDIUMBLOB","TINYBLOB","BINARY","VARBINARY",
  "DATE","TIME","DATETIME","TIMESTAMP","YEAR",
  "JSON","BIT","BOOLEAN","BOOL","ENUM","SET","GEOMETRY","POINT",
]);
const TYPE_MAP = { VARCHAR2: "VARCHAR", NUMBER: "DECIMAL", CLOB: "LONGTEXT", BLOB: "LONGBLOB", UUID: "CHAR(36)" };

function mapType(f) {
  const upper = String(f.type || "VARCHAR").toUpperCase();
  let base = TYPE_MAP[upper] || upper;
  if (base === "ENUM" && Array.isArray(f.values)) return `ENUM(${f.values.map(SAFE_LIT).join(",")})`;
  if (base === "SET"  && Array.isArray(f.values)) return `SET(${f.values.map(SAFE_LIT).join(",")})`;
  if (!TYPE_PASSTHRU.has(base) && !["VARCHAR2","NUMBER","CLOB","BLOB","UUID"].includes(upper)) base = "VARCHAR";
  if (["VARCHAR","CHAR","BINARY","VARBINARY"].includes(base)) return `${base}(${Number(f.size) || 255})`;
  if (["DECIMAL","NUMERIC"].includes(base)) return `${base}(${f.size || "10,0"})`;
  if (["INT","BIGINT","SMALLINT","TINYINT","MEDIUMINT"].includes(base) && f.size) return `${base}(${f.size})`;
  return base;
}

export function toMySQL(diagram) {
  const parts = [];
  parts.push("-- Generated by drawDB Desktop (MySQL Enhanced)");
  parts.push("-- Diagram: " + (diagram.name || ""));
  parts.push("-- Generated: " + new Date().toISOString());
  parts.push("SET FOREIGN_KEY_CHECKS=0;");
  parts.push("");

  (diagram.tables || []).forEach(t => {
    const lines = [];
    (t.fields || []).forEach(f => {
      let line = `  ${SAFE(f.name)} ${mapType(f)}`;
      if (f.notNull) line += " NOT NULL";
      if (f.default !== "" && f.default != null) line += ` DEFAULT ${formatDefault(f)}`;
      if (f.increment) line += " AUTO_INCREMENT";
      if (f.unique && !f.primary) line += " UNIQUE";
      if (f.onUpdate) line += ` ON UPDATE ${f.onUpdate}`;
      if (f.comment) line += ` COMMENT ${SAFE_LIT(f.comment)}`;
      if (f.check) line += ` CHECK (${f.check})`;
      lines.push(line);
    });
    const pkCols = (t.fields || []).filter(f => f.primary).map(f => SAFE(f.name));
    if (pkCols.length) lines.push(`  PRIMARY KEY (${pkCols.join(", ")})`);
    (t.indices || []).forEach(ix => {
      const u = ix.unique ? "UNIQUE " : "";
      lines.push(`  ${u}KEY ${SAFE(ix.name)} (${(ix.fields || []).map(SAFE).join(", ")})`);
    });
    const opts = ["ENGINE=InnoDB","DEFAULT CHARSET=utf8mb4","COLLATE=utf8mb4_unicode_ci",
                  t.comment ? `COMMENT=${SAFE_LIT(t.comment)}` : ""].filter(Boolean).join(" ");
    parts.push(`CREATE TABLE ${SAFE(t.name)} (\n${lines.join(",\n")}\n) ${opts};`);
    parts.push("");
  });

  (diagram.relationships || []).forEach((r, i) => {
    const sT = (diagram.tables || []).find(t => t.id === r.startTableId);
    const eT = (diagram.tables || []).find(t => t.id === r.endTableId);
    if (!sT || !eT) return;
    const sCol = sT.fields[r.startFieldId]?.name;
    const eCol = eT.fields[r.endFieldId]?.name;
    if (!sCol || !eCol) return;
    const name = SAFE(r.name || `fk_${sT.name}_${eT.name}_${i + 1}`);
    let line = `ALTER TABLE ${SAFE(sT.name)} ADD CONSTRAINT ${name} FOREIGN KEY (${SAFE(sCol)}) REFERENCES ${SAFE(eT.name)} (${SAFE(eCol)})`;
    const od = (r.deleteConstraint || "").toUpperCase();
    const ou = (r.updateConstraint || "").toUpperCase();
    if (od && od !== "NO ACTION") line += ` ON DELETE ${od}`;
    if (ou && ou !== "NO ACTION") line += ` ON UPDATE ${ou}`;
    parts.push(line + ";");
  });
  parts.push("");
  parts.push("SET FOREIGN_KEY_CHECKS=1;");
  return parts.join("\n");
}

function formatDefault(f) {
  const v = String(f.default);
  if (v === "CURRENT_TIMESTAMP") return "CURRENT_TIMESTAMP";
  if (/^-?\d+(\.\d+)?$/.test(v)) return v;
  if (/^(true|false)$/i.test(v)) return v.toLowerCase();
  if (v === "NULL") return "NULL";
  return SAFE_LIT(v);
}
