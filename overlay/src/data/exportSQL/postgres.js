// PostgreSQL SQL exporter for drawDB
// Mirrors the Oracle / MySQL exporters but emits idiomatic PostgreSQL:
//   - native ENUM types (CREATE TYPE ... AS ENUM)
//   - SERIAL / BIGSERIAL / SMALLSERIAL for auto-increment columns
//   - NUMERIC / BYTEA / JSONB / UUID / DOUBLE PRECISION
//   - COMMENT ON TABLE/COLUMN, CREATE [UNIQUE] INDEX, ALTER TABLE ... FK
const SAFE = (s) => `"${String(s).replace(/"/g, '""')}"`;
const SAFE_LIT = (s) => `'${String(s).replace(/'/g, "''")}'`;

const INT_TYPES = {
  INT: "INTEGER", INTEGER: "INTEGER", INT4: "INTEGER", MEDIUMINT: "INTEGER",
  BIGINT: "BIGINT", INT8: "BIGINT",
  SMALLINT: "SMALLINT", INT2: "SMALLINT", TINYINT: "SMALLINT", YEAR: "SMALLINT",
};
const SERIAL_FOR = { INTEGER: "SERIAL", BIGINT: "BIGSERIAL", SMALLINT: "SMALLSERIAL" };

const TYPE_MAP = {
  FLOAT: "REAL", REAL: "REAL", DOUBLE: "DOUBLE PRECISION", "DOUBLE PRECISION": "DOUBLE PRECISION",
  VARCHAR: "VARCHAR", VARCHAR2: "VARCHAR", CHAR: "CHAR", CHARACTER: "CHAR",
  TEXT: "TEXT", CLOB: "TEXT", LONGTEXT: "TEXT", MEDIUMTEXT: "TEXT", TINYTEXT: "VARCHAR(255)",
  BLOB: "BYTEA", LONGBLOB: "BYTEA", MEDIUMBLOB: "BYTEA", TINYBLOB: "BYTEA",
  BINARY: "BYTEA", VARBINARY: "BYTEA", RAW: "BYTEA", BYTEA: "BYTEA",
  DATE: "DATE", TIME: "TIME", DATETIME: "TIMESTAMP", TIMESTAMP: "TIMESTAMP",
  TIMESTAMPTZ: "TIMESTAMPTZ",
  BOOLEAN: "BOOLEAN", BOOL: "BOOLEAN", BIT: "BOOLEAN",
  JSON: "JSONB", JSONB: "JSONB", UUID: "UUID",
};

const enumTypeName = (table, field) => `${table}_${field}`;

function columnType(f) {
  const upper = String(f.type || "VARCHAR").toUpperCase();
  if (["DECIMAL", "NUMERIC", "NUMBER"].includes(upper)) {
    const s = String(f.size ?? "").trim();
    return s ? `NUMERIC(${s})` : "NUMERIC";
  }
  if (INT_TYPES[upper]) {
    const base = INT_TYPES[upper];
    if (f.increment && SERIAL_FOR[base]) return SERIAL_FOR[base];
    return base;
  }
  const mapped = TYPE_MAP[upper];
  if (mapped) {
    if (mapped === "VARCHAR" || mapped === "CHAR") {
      const sz = Number(f.size) || 255;
      return `${mapped}(${sz})`;
    }
    return mapped;
  }
  return upper;
}

export function toPostgres(diagram) {
  const parts = [];
  parts.push("-- Generated by drawDB Desktop (PostgreSQL dialect)");
  parts.push("-- Diagram: " + (diagram.name || ""));
  parts.push("-- Generated: " + new Date().toISOString());
  parts.push("");

  const namedEnums = new Map();
  (diagram.enums || []).forEach((e) => {
    if (e && e.name) namedEnums.set(String(e.name).toLowerCase(), e);
  });

  // 1. Composite object types
  (diagram.types || []).forEach((t) => {
    const cols = (t.fields || [])
      .map((f) => `  ${SAFE(f.name)} ${columnType(f)}`)
      .join(",\n");
    parts.push(`CREATE TYPE ${SAFE(t.name)} AS (\n${cols}\n);`);
  });

  // 2. ENUM types — named enums first, then per-column inline enums
  const definedEnums = new Set();
  (diagram.enums || []).forEach((e) => {
    if (!e || !e.name || !Array.isArray(e.values) || !e.values.length) return;
    const key = String(e.name).toLowerCase();
    if (definedEnums.has(key)) return;
    definedEnums.add(key);
    parts.push(`CREATE TYPE ${SAFE(e.name)} AS ENUM (${e.values.map(SAFE_LIT).join(", ")});`);
  });
  (diagram.tables || []).forEach((t) => {
    (t.fields || []).forEach((f) => {
      const upper = String(f.type || "").toUpperCase();
      if (upper === "ENUM" && Array.isArray(f.values) && f.values.length) {
        const name = enumTypeName(t.name, f.name);
        const key = name.toLowerCase();
        if (definedEnums.has(key)) return;
        definedEnums.add(key);
        parts.push(`CREATE TYPE ${SAFE(name)} AS ENUM (${f.values.map(SAFE_LIT).join(", ")});`);
      }
    });
  });
  if (definedEnums.size || (diagram.types || []).length) parts.push("");

  // 3. Tables
  (diagram.tables || []).forEach((t) => {
    const cols = [];
    const checks = [];
    (t.fields || []).forEach((f) => {
      const upper = String(f.type || "").toUpperCase();
      let typeStr;
      if (namedEnums.has(upper.toLowerCase())) {
        typeStr = SAFE(f.type);
      } else if (upper === "ENUM" && Array.isArray(f.values) && f.values.length) {
        typeStr = SAFE(enumTypeName(t.name, f.name));
      } else {
        typeStr = columnType(f);
      }
      const serial = /SERIAL$/.test(typeStr);
      let line = `  ${SAFE(f.name)} ${typeStr}`;
      if (!serial) {
        if (f.default !== "" && f.default != null) line += ` DEFAULT ${formatDefault(f)}`;
        if (f.notNull) line += " NOT NULL";
      }
      if (f.unique && !f.primary) line += " UNIQUE";
      if (f.check) checks.push(`CONSTRAINT ${SAFE("chk_" + t.name + "_" + f.name)} CHECK (${f.check})`);
      cols.push(line);
    });
    const pkCols = (t.fields || []).filter((f) => f.primary).map((f) => SAFE(f.name));
    if (pkCols.length) cols.push(`  CONSTRAINT ${SAFE("pk_" + t.name)} PRIMARY KEY (${pkCols.join(", ")})`);
    checks.forEach((c) => cols.push("  " + c));
    parts.push(`CREATE TABLE ${SAFE(t.name)} (\n${cols.join(",\n")}\n);`);

    if (t.comment) parts.push(`COMMENT ON TABLE ${SAFE(t.name)} IS ${SAFE_LIT(t.comment)};`);
    (t.fields || []).forEach((f) => {
      if (f.comment) parts.push(`COMMENT ON COLUMN ${SAFE(t.name)}.${SAFE(f.name)} IS ${SAFE_LIT(f.comment)};`);
    });
    (t.indices || []).forEach((ix) => {
      const u = ix.unique ? "UNIQUE " : "";
      parts.push(`CREATE ${u}INDEX ${SAFE(ix.name)} ON ${SAFE(t.name)} (${(ix.fields || []).map(SAFE).join(", ")});`);
    });
    parts.push("");
  });

  // 4. Foreign keys
  (diagram.relationships || []).forEach((r, i) => {
    const sT = (diagram.tables || []).find((t) => t.id === r.startTableId);
    const eT = (diagram.tables || []).find((t) => t.id === r.endTableId);
    if (!sT || !eT) return;
    const sCol = sT.fields[r.startFieldId]?.name;
    const eCol = eT.fields[r.endFieldId]?.name;
    if (!sCol || !eCol) return;
    const name = SAFE(r.name || `fk_${sT.name}_${eT.name}_${i + 1}`);
    let line = `ALTER TABLE ${SAFE(sT.name)} ADD CONSTRAINT ${name} FOREIGN KEY (${SAFE(sCol)}) REFERENCES ${SAFE(eT.name)} (${SAFE(eCol)})`;
    const od = (r.deleteConstraint || "").toUpperCase();
    const ou = (r.updateConstraint || "").toUpperCase();
    if (od && od !== "NO ACTION") line += ` ON DELETE ${od}`;
    if (ou && ou !== "NO ACTION") line += ` ON UPDATE ${ou}`;
    parts.push(line + ";");
  });

  return parts.join("\n");
}

function formatDefault(f) {
  const v = String(f.default);
  if (/^(CURRENT_TIMESTAMP|now\(\))$/i.test(v)) return "CURRENT_TIMESTAMP";
  if (/^-?\d+(\.\d+)?$/.test(v)) return v;
  if (/^(true|false)$/i.test(v)) return v.toLowerCase();
  if (v.toUpperCase() === "NULL") return "NULL";
  return SAFE_LIT(v);
}
