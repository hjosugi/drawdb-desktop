// Oracle SQL exporter for drawDB
const SAFE = (s) => `"${String(s).replace(/"/g, '""')}"`;
const SAFE_LIT = (s) => `'${String(s).replace(/'/g, "''")}'`;

const TYPE_MAP = {
  INT: "NUMBER(10)", INTEGER: "NUMBER(10)", BIGINT: "NUMBER(19)",
  SMALLINT: "NUMBER(5)", TINYINT: "NUMBER(3)",
  DECIMAL: null, NUMERIC: null, NUMBER: null, FLOAT: "BINARY_FLOAT", DOUBLE: "BINARY_DOUBLE", REAL: "BINARY_FLOAT",
  VARCHAR: "VARCHAR2", VARCHAR2: "VARCHAR2", CHAR: "CHAR", TEXT: "CLOB", LONGTEXT: "CLOB", MEDIUMTEXT: "CLOB", TINYTEXT: "VARCHAR2(255)",
  BLOB: "BLOB", LONGBLOB: "BLOB", MEDIUMBLOB: "BLOB", TINYBLOB: "RAW(255)", BINARY: "RAW", VARBINARY: "RAW",
  DATE: "DATE", TIME: "TIMESTAMP", DATETIME: "TIMESTAMP", TIMESTAMP: "TIMESTAMP",
  BOOLEAN: "NUMBER(1)", BOOL: "NUMBER(1)", BIT: "NUMBER(1)",
  JSON: "CLOB", UUID: "VARCHAR2(36)", ENUM: "VARCHAR2(255)",
};

function mapType(f) {
  const upper = String(f.type || "VARCHAR2").toUpperCase();
  const mapped = TYPE_MAP[upper];
  if (upper === "ENUM" && Array.isArray(f.values)) {
    return { type: `VARCHAR2(${Math.max(10, ...f.values.map(v => v.length))})`,
             check: `${SAFE(f.name)} IN (${f.values.map(SAFE_LIT).join(",")})` };
  }
  if (mapped === null) {
    const s = String(f.size || "10,0");
    return { type: `NUMBER(${s})` };
  }
  if (mapped) {
    if (mapped === "VARCHAR2" || mapped === "CHAR") {
      const sz = Number(f.size) || 255;
      return { type: `${mapped}(${Math.min(4000, sz)})` };
    }
    if (mapped === "RAW") return { type: `RAW(${Number(f.size) || 2000})` };
    return { type: mapped };
  }
  return { type: upper };
}

export function toOracle(diagram) {
  const parts = [];
  parts.push("-- Generated by drawDB Desktop (Oracle dialect)");
  parts.push("-- Diagram: " + (diagram.name || ""));
  parts.push("-- Generated: " + new Date().toISOString());
  parts.push("");

  (diagram.types || []).forEach(t => {
    const cols = (t.fields || []).map(f => {
      const m = mapType(f);
      return `  ${SAFE(f.name)} ${m.type}`;
    }).join(",\n");
    parts.push(`CREATE OR REPLACE TYPE ${SAFE(t.name)} AS OBJECT (\n${cols}\n);\n/`);
  });

  (diagram.tables || []).forEach(t => {
    const cols = [];
    const inlineChecks = [];
    (t.fields || []).forEach(f => {
      const m = mapType(f);
      let line = `  ${SAFE(f.name)} ${m.type}`;
      if (f.default !== "" && f.default != null) line += ` DEFAULT ${formatDefault(f)}`;
      if (f.notNull) line += " NOT NULL";
      if (f.increment) line += " GENERATED BY DEFAULT AS IDENTITY";
      if (f.unique && !f.primary) line += " UNIQUE";
      if (f.check) inlineChecks.push(`CONSTRAINT ${SAFE("chk_" + t.name + "_" + f.name)} CHECK (${f.check})`);
      if (m.check) inlineChecks.push(`CONSTRAINT ${SAFE("chk_" + t.name + "_" + f.name + "_enum")} CHECK (${m.check})`);
      cols.push(line);
    });
    const pkCols = (t.fields || []).filter(f => f.primary).map(f => SAFE(f.name));
    if (pkCols.length) cols.push(`  CONSTRAINT ${SAFE("pk_" + t.name)} PRIMARY KEY (${pkCols.join(", ")})`);
    inlineChecks.forEach(c => cols.push("  " + c));
    parts.push(`CREATE TABLE ${SAFE(t.name)} (\n${cols.join(",\n")}\n);`);
    if (t.comment) parts.push(`COMMENT ON TABLE ${SAFE(t.name)} IS ${SAFE_LIT(t.comment)};`);
    (t.fields || []).forEach(f => {
      if (f.comment) parts.push(`COMMENT ON COLUMN ${SAFE(t.name)}.${SAFE(f.name)} IS ${SAFE_LIT(f.comment)};`);
    });
    (t.indices || []).forEach(ix => {
      const u = ix.unique ? "UNIQUE " : "";
      parts.push(`CREATE ${u}INDEX ${SAFE(ix.name)} ON ${SAFE(t.name)} (${(ix.fields || []).map(SAFE).join(", ")});`);
    });
    parts.push("");
  });

  (diagram.relationships || []).forEach((r, i) => {
    const sT = (diagram.tables || []).find(t => t.id === r.startTableId);
    const eT = (diagram.tables || []).find(t => t.id === r.endTableId);
    if (!sT || !eT) return;
    const sCol = sT.fields[r.startFieldId]?.name;
    const eCol = eT.fields[r.endFieldId]?.name;
    if (!sCol || !eCol) return;
    const name = SAFE(r.name || `fk_${sT.name}_${eT.name}_${i + 1}`.slice(0, 30));
    let line = `ALTER TABLE ${SAFE(sT.name)} ADD CONSTRAINT ${name} FOREIGN KEY (${SAFE(sCol)}) REFERENCES ${SAFE(eT.name)} (${SAFE(eCol)})`;
    const od = (r.deleteConstraint || "").toUpperCase();
    if (od === "CASCADE")  line += " ON DELETE CASCADE";
    if (od === "SET NULL") line += " ON DELETE SET NULL";
    parts.push(line + ";");
  });
  return parts.join("\n");
}

function formatDefault(f) {
  const v = String(f.default);
  if (v === "CURRENT_TIMESTAMP") return "CURRENT_TIMESTAMP";
  if (/^-?\d+(\.\d+)?$/.test(v)) return v;
  if (/^(true|false)$/i.test(v)) return v.toLowerCase() === "true" ? "1" : "0";
  return SAFE_LIT(v);
}
